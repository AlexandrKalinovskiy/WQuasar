        [HttpGet]
        public ActionResult Roles()
        {
            var roles = _roleManager.Roles.ToList();

            var model = new List<EditRolesViewModel>();

            foreach(var role in roles)
            {
                model.Add(new EditRolesViewModel
                {
                    Id = role.Id,
                    Name = role.Name
                });
            }

            return View(model);
        }

